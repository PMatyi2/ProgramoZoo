/*This makes things appear when scrolling down*/
(function ($) {
  $(document).ready(function(){

    // hide .navbar first
    $(".footer").hide();

    // fade in .navbar
    $(function () {
        $(window).scroll(function () {

                 // set distance user needs to scroll before we start fadeIn
            if ($(this).scrollTop() > 100) {
                $('.footer').fadeIn();
            } else {
                $('.footer').fadeOut();
            }
        });
    });

});
  }(jQuery));